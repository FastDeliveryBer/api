import mongoose from 'mongoose'

const captain = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: Number,
    required: true,
  },
})

const Captain = mongoose.model('captain', captain)

export { Captain }
